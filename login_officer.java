    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String email = txtEmail.getText();
    String plainpass = new String(txtPassword.getPassword());

    try (Connection connection = DbConnection.getConnection()) {
          String query = "SELECT * FROM officers WHERE email = ?";
        java.sql.PreparedStatement pst = connection.prepareStatement(query);
        pst.setString(1, email);
        java.sql.ResultSet rs = pst.executeQuery();

        if (rs.next()) {
           String storedPassword = rs.getString("password");

                if (plainpass.equals(storedPassword)) {
                // Passwords match â€” login success
                // create student object from rs fields
                int id = rs.getInt("id");
                String name = rs.getString("officer_name");
                String position = rs.getString("position");
                String mobile = rs.getString("mobile_num");

                officers fetchedOfficers = new officers(id, email, storedPassword,  name, position, mobile);
                sessionOfficer.loggedInOfficers = fetchedOfficers;

                JOptionPane.showMessageDialog(this, "Login Successful!");
                new officer_profile(email).setVisible(true);
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Invalid Email or Password!", "Login Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Invalid Email or Password!", "Login Error", JOptionPane.ERROR_MESSAGE);
        }
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Database Connection Error!", "Error", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_btnLoginActionPerformed

