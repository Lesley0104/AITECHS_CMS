    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
       String searchTerm = txtSearch.getText().trim();

    if (searchTerm.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter a search term.");
        return;
    }

    String sql = "SELECT invoice_number, student_email, student_name, amount, method, status, payment_date, officer_name " +
                 "FROM payed " +
                 "WHERE student_email LIKE ? OR student_name LIKE ? OR invoice_number LIKE ?";

    try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/aitechs_cms_db", "root", "");
         PreparedStatement pst = conn.prepareStatement(sql)) {

        String wildcardSearch = "%" + searchTerm + "%";
        pst.setString(1, wildcardSearch);
        pst.setString(2, wildcardSearch);
        pst.setString(3, wildcardSearch);

        ResultSet rs = pst.executeQuery();
        DefaultTableModel model = (DefaultTableModel) tblPaymentHistory.getModel();
        model.setRowCount(0);

        boolean hasResults = false;
        while (rs.next()) {
            hasResults = true;
            model.addRow(new Object[]{
                rs.getString("invoice_number"),
                rs.getString("student_email"),
                rs.getString("student_name"),
                rs.getDouble("amount"),
                rs.getString("method"),
                rs.getString("status"),
                rs.getTimestamp("payment_date"),
                rs.getString("officer_name")
            });
        }

        if (!hasResults) {
            JOptionPane.showMessageDialog(this, "No results found for: " + searchTerm);
        }

    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Database error: " + ex.getMessage());
    }
    }//GEN-LAST:event_btnSearchActionPerformed

