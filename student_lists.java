    private void loadStudentTable(String keyword) {
    String query = "SELECT student_num, student_name, year_lvl, block FROM students "
                 + "WHERE student_num LIKE ? OR student_name LIKE ? OR year_lvl LIKE ? OR block LIKE ?";

    try (PreparedStatement statement = connection.prepareStatement(query)) {
        String searchKey = "%" + keyword.trim() + "%";
        for (int i = 1; i <= 4; i++) {
            statement.setString(i, searchKey);
        }

        ResultSet rs = statement.executeQuery();
        DefaultTableModel model = (DefaultTableModel) tblStudentList.getModel();
        model.setRowCount(0); // Clear table

        int studentCount = 0;
        
        while (rs.next()) {
            String studentNo = rs.getString("student_num");
            String name = rs.getString("student_name");
            String yearLevel = rs.getString("year_lvl");
            String block = rs.getString("block");

            model.addRow(new Object[]{studentNo, name, yearLevel, block});
            studentCount++;
        }

        // Display total number of students
        total_student.setText(String.valueOf(studentCount));
        total_student.setEditable(false); // Make it read-only

    } catch (SQLException ex) {
        System.out.println("Search error: " + ex.getMessage());
    }
}
   

