    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
    int selectedRow = tblStudentList.getSelectedRow();
    
    if (selectedRow == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a student to delete.");
        return;
    }

    // Confirm deletion
    int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this student?", "Confirm Deletion", javax.swing.JOptionPane.YES_NO_OPTION);
    if (confirm != javax.swing.JOptionPane.YES_OPTION) {
        return;
    }

    // Get student number from selected row
    String studentNo = tblStudentList.getValueAt(selectedRow, 0).toString();

    // Delete student from database
    String deleteQuery = "DELETE FROM students WHERE student_num = ?";
    try (PreparedStatement stmt = connection.prepareStatement(deleteQuery)) {
        stmt.setString(1, studentNo);
        int rowsAffected = stmt.executeUpdate();

        if (rowsAffected > 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Student deleted successfully.");
            loadStudentTable(""); // Refresh table
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Failed to delete student.");
        }

    } catch (SQLException ex) {
        javax.swing.JOptionPane.showMessageDialog(this, "Database error: " + ex.getMessage());
    }


    }//GEN-LAST:event_btnDeleteActionPerformed

