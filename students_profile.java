private void loadStudentProfile() {
        try (Connection connection = DbConnection.getConnection()) {
            String query = "SELECT student_num, student_name, year_lvl, block, mobile_num, address, clearances, requested_at FROM students WHERE email = ?";
            PreparedStatement pst = connection.prepareStatement(query);
            pst.setString(1, userEmail);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                txtStudentnum.setText(rs.getString("student_num"));
                txtName.setText(rs.getString("student_name"));
                cbYearLvl.setSelectedItem(rs.getString("year_lvl"));
                cbBlock.setSelectedItem(rs.getString("block"));
                txtMobileNum.setText(rs.getString("mobile_num"));
                txtAddress.setText(rs.getString("address"));
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading profile: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
