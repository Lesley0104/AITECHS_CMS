    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
          
        if (userEmail == null || userEmail.isEmpty()) {
        JOptionPane.showMessageDialog(this, "No email specified. Cannot save.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    String studentNo = txtStudentnum.getText().trim();
    String name = txtName.getText().trim();
    String yearLevel = cbYearLvl.getSelectedItem().toString();
    String block = cbBlock.getSelectedItem().toString();
    String mobile = txtMobileNum.getText().trim();
    String address = txtAddress.getText().trim();

    if (studentNo.isEmpty() || name.isEmpty() || yearLevel.isEmpty() || block.isEmpty() ||  mobile.isEmpty() || address.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields.", "Input Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
     // Validate mobile number (e.g., PH format)
        if (!mobile.matches("09\\d{9}")) {
            JOptionPane.showMessageDialog(this, "Invalid mobile number format. Expected: 09XXXXXXXXX", "Input Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
    
      try (Connection connection = DbConnection.getConnection()) {
            // Check if profile already exists for this email
            String checkQuery = "SELECT COUNT(*) FROM students WHERE email = ?";
            PreparedStatement checkStmt = connection.prepareStatement(checkQuery);
            checkStmt.setString(1, userEmail);
            ResultSet rs = checkStmt.executeQuery();

            if (rs.next() && rs.getInt(1) > 0) {
                // Update existing profile
                String updateQuery = "UPDATE students SET student_num=?, student_name=?, year_lvl=?, block=?, mobile_num=?, address=? WHERE email=?";
                PreparedStatement pst = connection.prepareStatement(updateQuery);
                pst.setString(1, studentNo);
                pst.setString(2, name);
                pst.setString(3, yearLevel);
                pst.setString(4, block);
                pst.setString(5, mobile);
                pst.setString(6, address);
                pst.setString(7, userEmail);
                pst.executeUpdate();

                int rowsUpdated = pst.executeUpdate();
                if (rowsUpdated > 0) {
                    int result = JOptionPane.showConfirmDialog(this, "Does all your information are correct?","Confirm", JOptionPane.YES_NO_OPTION);
if (result == JOptionPane.YES_OPTION) {
    new student_dashboard().setVisible(true);
    this.dispose();
}
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to update profile.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }else {
            // Insert new profile (optional)
            String insertQuery = "INSERT INTO students (email, student_num, student_name, year_lvl, block, mobile_num, address) VALUES (?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement insertStmt = connection.prepareStatement(insertQuery);
            insertStmt.setString(1, userEmail);
            insertStmt.setString(2, studentNo);
            insertStmt.setString(3, name);
            insertStmt.setString(4, yearLevel);
            insertStmt.setString(5, block);
            insertStmt.setString(6, mobile);
            insertStmt.setString(7, address);
           

            int rowsInserted = insertStmt.executeUpdate();
            if (rowsInserted > 0) {
                JOptionPane.showMessageDialog(this, "Profile created successfully.");
               
student updatedStudent = student_dbconnect.getStudentByEmail(userEmail); // get latest from DB
session.loggedInStudent = updatedStudent; 
//                new student_dashboard().setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to create profile.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
            
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }

        
    }//GEN-LAST:event_btnSaveActionPerformed

